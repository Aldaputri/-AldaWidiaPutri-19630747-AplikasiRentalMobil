     public ArrayList<Trans> getTransList(String keyword){
     ArrayList<Trans> transList = new ArrayList<Trans>();
     Koneksi koneksi =  new Koneksi();
     Connection connection = koneksi.getConnection();
     
     String query ="SELECT * FROM Transaksi "+keyword;
     Statement st;
     ResultSet rs;
     
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         while (rs.next()){
             trans = new Trans(
                     rs.getInt("no"),
                     rs.getString("lama_sewa"),
                     rs.getString("harga"),
                     rs.getString("merek_mobil"),
                     rs.getString("total"));
             transList.add(trans);
         }
     } catch (SQLException | NullPointerException ex){
         System.err.println("Koneksi Null Gagal");
     }
        return transList;
    }
     
      public void selectTrans(String keyword){
        ArrayList<Trans> list = getTransList(keyword);
        DefaultTableModel model = (DefaultTableModel)tabeltransaksi.getModel();
        Object[] row = new Object[5];
        
        for (int i = 0; i <list.size(); i++){
            row[0] = list.get(i).getNo();
            row[1] = list.get(i).getLama_sewa();
            row[2] = list.get(i).getHarga();
            row[3] = list.get(i).getMerek_mobil();
            row[4] = list.get(i).getTotal();

            
            model.addRow(row);
        }
    }
      
       public final void resetTable(String keyword){
        DefaultTableModel model = (DefaultTableModel)tabeltransaksi.getModel();
        model.setRowCount(0);
        selectTrans(keyword);
    }

