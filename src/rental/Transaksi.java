    private void jSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSimpanActionPerformed
        // TODO add your handling code here:
        String no = txt_no.getText().toString().trim();
        String lama_sewa = txt_lama.getText().toString().trim();
        String harga = txt_harga.getText().toString().trim();
        String merek_mobil = cbmobil.getSelectedItem().toString().trim();
        String total = txt_total.getText().toString().trim();
        try{
            Koneksi koneksi =  new Koneksi();
            Connection c = koneksi.getConnection();
            String sql = "INSERT INTO transaksi VALUES (?,?,?,?,?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, no);
            p.setString(2, lama_sewa);
            p.setString(3, harga);
            p.setString(4, merek_mobil);
            p.setString(5, total);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Behasil Di Simpan");
            resetTable("");
        }catch (SQLException e){
            System.err.println(e);
        }
    }//GEN-LAST:event_jSimpanActionPerformed

