    private void JHapus1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JHapus1ActionPerformed
        // TODO add your handling code here:
        int i = tabelpenyewa.getSelectedRow();
            int pilihan = JOptionPane.showConfirmDialog(
                    null,
                    "Data Di Hapus ?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION);
            if(pilihan==0){
                if(i>=0){
                    try{
                        TableModel mode = tabelpenyewa.getModel();
                        Koneksi koneksi = new Koneksi();
                        Connection con  = koneksi.getConnection();
                        String executeQuery = "delete from penyewa where no=?";
                        PreparedStatement ps = con.prepareStatement(executeQuery);
                        ps.setString(1, mode.getValueAt(i,0).toString());
                        ps.executeUpdate();
                    }catch(SQLException ex){
                        System.err.println(ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus"); 
                }
            }
            resetTable("");
    }//GEN-LAST:event_JHapus1ActionPerformed

