    public ArrayList<Sewa> getSewaList(String keyword){
     ArrayList<Sewa> sewaList = new ArrayList<Sewa>();
     Koneksi koneksi =  new Koneksi();
     Connection connection = koneksi.getConnection();
     
     String query ="SELECT * FROM penyewa"+keyword;
     Statement st;
     ResultSet rs;
     
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         while (rs.next()){
             sewa = new Sewa(
                     rs.getInt("no"),
                     rs.getString("nama"),
                     rs.getString("alamat"),
                     rs.getString("no_telepon"),
                     rs.getString("tanggal"));
             sewaList.add(sewa);
         }
     } catch (SQLException | NullPointerException ex){
         System.err.println("Koneksi Null Gagal");
     }
        return sewaList;
    }
    
    public void selectSewa(String keyword){
        ArrayList<Sewa> list = getSewaList(keyword);
        DefaultTableModel model = (DefaultTableModel)tabelpenyewa.getModel();
        Object[] row = new Object[5];
        
        for (int i = 0; i <list.size(); i++){
            row[0] = list.get(i).getNo();
            row[1] = list.get(i).getNama();
            row[2] = list.get(i).getAlamat();
            row[3] = list.get(i).getNo_telepon();
            row[4] = list.get(i).getTanggal();
            
            model.addRow(row);
        }
    }
    
    public final void resetTable(String keyword){
        DefaultTableModel model = (DefaultTableModel)tabelpenyewa.getModel();
        model.setRowCount(0);
        selectSewa(keyword);
    }

